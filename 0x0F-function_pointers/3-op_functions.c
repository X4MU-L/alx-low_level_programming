/**
 * op_mul - Multiplies two ints
 * @a: int to multiply
 * @b: int to multiply
 * Return: Returns the result of multipling a by b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_add - Adds two ints
 * @a: int to add
 * @b: int to add
 * Return: Returns the result of adding a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two ints
 * @a: int to subtract
 * @b: int to subtract
 * Return: Returns result of subtracting b from a
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - Divides two ints
 * @a: int to divide
 * @b: int to divide
 * Return: Returns result of dividing a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Gets the remainder of two ints
 * @a: int
 * @b: int
 * Return: Returns the remainder of a mod b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
